#include "math/vec2.h"

struct Player;
struct Renderer;

struct Scene {
    int cols;
    int rows;
    Vec2 spawn;     // player position set by enter()
    const int *map; // flat row-major: map[row * cols + col]

    static constexpr Scene New(const int *map, int cols, int rows, Vec2 spawn) {
        Scene s;
        s.map = map;
        s.cols = cols;
        s.rows = rows;
        s.spawn = spawn;
        return s;
    }

    // Out-of-bounds returns 1 (solid wall) to keep the DDA inside the map.
    int cell_at(int col, int row) const;
    bool in_bounds(int col, int row) const;
    // Cell 5 (portal) is passable — is_wall returns false for it.
    bool is_wall(int col, int row) const;

    // Set player.position to this scene's spawn point.
    void enter(Player &player) const;
    // Render the top-down minimap overlay into r's framebuffer (top-left corner).
    void render_minimap(Renderer &r, const Player &player, int win_w, int win_h) const;
};

