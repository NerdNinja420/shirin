#include "render/renderer.h"
#include "utils/logger.h"

#include <cmath>
#include <cstdlib>

Renderer Renderer::New(SDL_Window *win) {
    Renderer r;
    r.sdl_renderer = SDL_CreateRenderer(win, nullptr);
    if (!r.sdl_renderer) {
        logger::error("Renderer::New: SDL_CreateRenderer failed: %s", SDL_GetError());
        SDL_Quit();
        exit(1);
    }
    return r;
}

void Renderer::destroy() {
    SDL_DestroyRenderer(this->sdl_renderer);
    this->sdl_renderer = nullptr;
}

void Renderer::set_color(Color c) {
    SDL_SetRenderDrawColor(this->sdl_renderer, c.r, c.g, c.b, 255);
}

void Renderer::clear() {
    SDL_RenderClear(this->sdl_renderer);
}

void Renderer::plot(int x, int y) {
    SDL_RenderPoint(this->sdl_renderer, (float)x, (float)y);
}

void Renderer::draw_line(float x0, float y0, float x1, float y1) {
    SDL_RenderLine(this->sdl_renderer, (int)x0, (int)y0, (int)x1, (int)y1);
}

void Renderer::fill_rect(float x, float y, float w, float h) {
    SDL_FRect rect{x, y, w, h};
    SDL_RenderFillRect(this->sdl_renderer, &rect);
}

void Renderer::draw_rect(float x, float y, float w, float h) {
    SDL_FRect rect{x, y, w, h};
    SDL_RenderRect(this->sdl_renderer, &rect);
}

