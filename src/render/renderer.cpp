void Renderer::fill_column_aa(float x,
                              float w,
                              float y_top,
                              float y_bottom,
                              Color wall,
                              Color ceiling,
                              Color floor) {
    int top_solid = (int)ceilf(y_top);
    int bot_solid = (int)floorf(y_bottom);
    float top_frac = (float)top_solid - y_top;
    float bot_frac = y_bottom - floorf(y_bottom);

    Color top_c = ceiling.lerp(wall, top_frac);
    SDL_SetRenderDrawColor(this->sdl_renderer, top_c.r, top_c.g, top_c.b, 255);
    SDL_FRect top_row{x, (float)(top_solid - 1), w, 1.0f};
    SDL_RenderFillRect(this->sdl_renderer, &top_row);

    SDL_SetRenderDrawColor(this->sdl_renderer, wall.r, wall.g, wall.b, 255);
    SDL_FRect body{x, (float)top_solid, w, (float)(bot_solid - top_solid)};
    SDL_RenderFillRect(this->sdl_renderer, &body);

    Color bot_c = wall.lerp(floor, 1.0f - bot_frac);
    SDL_SetRenderDrawColor(this->sdl_renderer, bot_c.r, bot_c.g, bot_c.b, 255);
    SDL_FRect bot_row{x, (float)bot_solid, w, 1.0f};
    SDL_RenderFillRect(this->sdl_renderer, &bot_row);
}

